/**
 * Extended GraphTraversalSource for TorcDB.
 *
 * @author Jonathan Ellithorpe (jde@cs.stanford.edu)
 */
public class TorcGraphTraversalSource extends GraphTraversalSource {

  public TorcGraphTraversalSource(final TorcGraph graph) {
    super(graph);
  }

  public TorcGraphTraversalSource clone() {
    final TorcGraphTraversalSource clone = (TorcGraphTraversalSource) super.clone();
    return clone;
  }

  @Override
  public GraphTraversal<Vertex, Vertex> V(final Object... vertexIds) {
    final TorcGraphTraversalSource clone = this.clone();
    final GraphTraversal.Admin<Vertex, Vertex> traversal = new TorcGraphTraversal<Vertex, Vertex>(clone);
    return traversal.addStep(new GraphStep<>(traversal, Vertex.class, true, vertexIds));
  }

}
